#include "calc.h"

/**
 * op_add - returns the sum of two numbers
 * @a: the first
 * @b: the second
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the sub of two numbers
 * @a: the first
 * @b: the second
 * Return: 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the multiplication of two numbers
 * @a: the first
 * @b: the second
 * Return: 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the multiplication of two numbers
 * @a: the first
 * @b: the second
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of two num
 * @a: the first
 * @b: the second
 * Return: 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
